int		ft_max(int *tab, int length)
{
	int		z;
	int		stock;

	stock = -2147483648;
	z = 0;
	while (z < length)
	{
		if (stock <= tab[z])
			stock = tab[z];
		z++;
	}
	return (stock);
}
