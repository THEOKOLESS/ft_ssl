#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	size_t	i;

	i = 0;
	if (!s1 || !s2)
		return (0);
	if (n == 0)
		return (1);
	while (*s1 == *s2 && i < n)
	{
		if ((!*s1 && !*s2) || i + 1 == n)
			return (1);
		s1++;
		s2++;
		i++;
	}
	return (0);
}
