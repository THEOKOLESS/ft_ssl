#include "libft.h"

void	ft_putnbr(int n)
{
	long	tmp;

	tmp = n;
	if (tmp < 0)
	{
		ft_putchar('-');
		tmp *= -1;
	}
	if (tmp > 9)
		ft_putnbr(tmp / 10);
	ft_putchar(tmp % 10 + '0');
}
